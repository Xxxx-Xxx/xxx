public enum ElevatorControllerAction {
    /**
     * Sets when starting transportation
     */
    STARTING_TRANSPORTATION,
    /**
     * Sets when transportation is complete
     */
    COMPLETION_TRANSPORTATION,
    /**
     * Sets when elevator moving
     */
    MOVING_ELEVATOR,
    /**
     * Used when boarding of passenger
     */
    BOARDING_OF_PASSENGER,
    /**
     * Sets when passengers leave the elevator
     */
    DEBOARDING_OF_PASSENGER;
}

